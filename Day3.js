// Write a program to:
// 1.	Check whether a number is positive, negative, or zero.
// 2.	Print appropriate message.

let num = -5;
if (num > 0){
    console.log(`The number ${num} is POSITIVE`);
}
else if (num === 0){
    console.log(`The number ${num} is ZERO`);
}
else {
    console.log(`The number ${num} is NEGATIVE`);
}